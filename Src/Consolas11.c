bool  Consolas11_GetCharData(char ch, lpFontChar_t *fc) {
	int index = -1;
	int cnt = 223;
	//
	for(int i = 0; i < cnt; i++) {
		if(_Consolas11[i].Code != ch)
			continue;
		//
		index = i;
		*fc = (FontChar_t*)&_Consolas11[i];
		break;
	}
	if(index < 0)
		*fc = NULL;
	//
	return (index >= 0);
}



